package com.springcore.refrence;

public class B {

private int num;

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public B() {
    }

    @Override
    public String toString() {
        return "B{" +
                "num=" + num +
                '}';
    }
}
